package com.example.hello_world;

public class Parameters {

    private double initialAmountMoney;
    private double monthlyContribution;
    private double interestRate;
    private double yearsInvesting;


    public double getInitialAmountMoney() {
        return initialAmountMoney;
    }

    public void setInitialAmountMoney(double initialAmountMoney) {
        this.initialAmountMoney = initialAmountMoney;
    }

    public double getMonthlyContribution() {
        return monthlyContribution;
    }

    public void setMonthlyContribution(double monthlyContribution) {
        this.monthlyContribution = monthlyContribution;
    }

    public double getInterestRate() {
        return interestRate;
    }

    public void setInterestRate(double interestRate) {
        this.interestRate = interestRate;
    }

    public double getYearsInvesting() {
        return yearsInvesting;
    }

    public void setYearsInvesting(double yearsInvesting) {
        this.yearsInvesting = yearsInvesting;
    }
}
